#include "util.h"

// 函数power_of_two用于判断一个整数是不是2的整数次幂
int power_of_two(int n) {
    if (n <= 0)
        return 0;
    return (n & (n - 1)) == 0;
}

void loadTexture(const char *file_name, GLuint &texture) {
    GLint width, height, total_bytes;
    GLubyte *pixels = nullptr;

    // 打开文件，如果失败，返回
    FILE *pFile = fopen(file_name, "rb");
    if (pFile == nullptr)
        return;

    // 读取文件中图象的宽度和高度
    fseek(pFile, 0x0012, SEEK_SET);
    fread(&width, 4, 1, pFile);
    fread(&height, 4, 1, pFile);
    fseek(pFile, BMP_Header_Length, SEEK_SET);

    // 计算每行像素所占字节数，并根据此数据计算总像素字节数
    {
        GLint line_bytes = width * 3;
        while (line_bytes % 4 != 0)
            ++line_bytes;
        total_bytes = line_bytes * height;
    }

    // 根据总像素字节数分配内存
    pixels = (GLubyte *) malloc((size_t) total_bytes);
    if (pixels == nullptr) {
        fclose(pFile);
        return;
    }

    // 读取像素数据
    if (fread(pixels, (size_t) total_bytes, 1, pFile) <= 0) {
        free(pixels);
        fclose(pFile);
        return;
    }

    // 对就旧版本的兼容，如果图象的宽度和高度不是的整数次方，则需要进行缩放
    // 若图像宽高超过了OpenGL规定的最大值，也缩放
    {
        GLint max;
        glGetIntegerv(GL_MAX_TEXTURE_SIZE, &max);
        if (!power_of_two(width)
            || !power_of_two(height)
            || width > max
            || height > max) {
            const GLint new_width = 256;
            const GLint new_height = 256; // 规定缩放后新的大小为边长的正方形
            GLint new_line_bytes, new_total_bytes;
            GLubyte *new_pixels = nullptr;

            // 计算每行需要的字节数和总字节数
            new_line_bytes = new_width * 3;
            while (new_line_bytes % 4 != 0)
                ++new_line_bytes;
            new_total_bytes = new_line_bytes * new_height;

            // 分配内存
            new_pixels = (GLubyte *) malloc((size_t) new_total_bytes);
            if (new_pixels == nullptr) {
                free(pixels);
                fclose(pFile);
                return;
            }

            // 进行像素缩放
            gluScaleImage(GL_RGB,
                          width, height, GL_UNSIGNED_BYTE, pixels,
                          new_width, new_height, GL_UNSIGNED_BYTE, new_pixels);

            // 释放原来的像素数据，把pixels指向新的像素数据，并重新设置width和height
            free(pixels);
            pixels = new_pixels;
            width = new_width;
            height = new_height;
        }
    }

    glGenTextures(1, &texture);
    glBindTexture(GL_TEXTURE_2D, texture);
    gluBuild2DMipmaps(GL_TEXTURE_2D, 4, width, height, GL_BGR_EXT, GL_UNSIGNED_BYTE, pixels);

    free(pixels);
    fclose(pFile);
}

std::string& trim(std::string &s)
{
    if (s.empty())
    {
        return s;
    }

    s.erase(0, s.find_first_not_of(" "));
    s.erase(s.find_last_not_of(" ") + 1);
    return s;
}
